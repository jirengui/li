package ruanjianbei.sport.mysport.bean;

public class JuBanSaiShiBean {

	private int uid;//举办人id
	private String time;//举办的时间
	private String kaishitime;//比赛开始时间
	private String jieshutime;//比赛结束时间
	private String place;//举报地点
	private String context;//文本
	private String liwu;//奖励的礼物
	private String tupian;
	
	public int getUid() {
		return uid;
	}
	public void setUid(int uid) {
		this.uid = uid;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getPlace() {
		return place;
	}
	public void setPlace(String place) {
		this.place = place;
	}
	public String getContext() {
		return context;
	}
	public void setContext(String context) {
		this.context = context;
	}
	public String getLiwu() {
		return liwu;
	}
	public void setLiwu(String liwu) {
		this.liwu = liwu;
	}
	public String getTupian() {
		return tupian;
	}
	public void setTupian(String tupian) {
		this.tupian = tupian;
	}
	public String getKaishitime() {
		return kaishitime;
	}
	public void setKaishitime(String kaishitime) {
		this.kaishitime = kaishitime;
	}
	public String getJieshutime() {
		return jieshutime;
	}
	public void setJieshutime(String jieshutime) {
		this.jieshutime = jieshutime;
	}
	
	
}
